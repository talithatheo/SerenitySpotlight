                        <h3>Recommended Songs</h3>
                        <p>Here Comes the Sun - The Beatless
                            Pelangi - HiVi! 
                        </p>
                        <div class="read-more-cont">
                            <p>Maintaining mental health amidst the hustle and bustle of life is crucial. Music holds therapeutic
                                power that can calm the mind and uplift the mood. This article presents a list of recommended songs
                                to help you feel better, offering peace, happiness, and inspiration through touching melodies and lyrics.
                                Discover songs that can become your faithful companions on the journey to better mental well-being.</p>
                            
                            <h2>1. Yura Yunita – Tenang</h2>
                            <p>The song "Tenang" is about self-reflection. Yura expresses that often we feel uneasy, anxious,
                                have many questions about the future, and regret the past, making us feel restless. Yura also
                                mentions that the song contains a prayer to help make life calmer.</p>
                            
                            <h2>2. Yura Yunita – Tutur Batin</h2>
                            <p>Apart from "Tenang," "Tutur Batin" is about self-acceptance. In this song, Yura Yunita wants to
                                convey that perfection is when we can accept both our strengths and weaknesses. The music video
                                explains that every woman has her own uniqueness. For Yura, "Tutur Batin" is a special work
                                because she was on a journey to accept and forgive all the traumas she experienced in the past
                                during its creation.
                            </p>
                            
                            <h2>3. Breathe - Lee Hi</h2>
                            <p>The song “Breathe” tells about someone’s struggle to survive. The pressures and forces make life
                                so difficult that it feels like it's hard to breathe.
                            </p>
                            
                            <h2>4. 1-800-273-8255 - Logic feat. Alessia Cara, Khalid</h2>
                            <p>"1-800-273-8255" was released by Logic in 2018. This song collaborates with the National Suicide Prevention
                                Lifeline in the United States. The lyrics are very relevant to anyone who has ever felt or is feeling the
                                urge to end their life. Through this song, Logic hopes people do not act rashly. Interestingly,
                                the title "1-800-273-8255" is the phone number for the American National Suicide Prevention Lifeline,
                                where people experiencing emotional distress or suicidal crises can get help.
                            </p>

                            <h2>5. Ariana Grande - Breathin</h2>
                            <p>Released in 2018 as part of her fourth album "Sweetener," this song was written by Ariana Grande, Savan Kotecha,
                                Peter Svensson, and Ilya Salmanzadeh. In an interview on The Tonight Show Starring Jimmy Fallon in 2018,
                                Ariana mentioned that the song was written to depict her state of anxiety. "Breathin is about breathing...
                                when you’re anxious. It’s about anxiety and feeling like you can’t get a full breath. It’s the worst feeling in
                                the world. This song is about that feeling," said Ariana Grande. Here’s a snippet of the lyrics:
                                "Some days, things just take way too much of my energy
                                I look up and the whole room's spinning"
                            </p>
                            
                            <h2>6. Shawn Mendes - In My Blood</h2>
                            <p>"In My Blood" by Shawn Mendes talks about how he deals with anxiety. The song was written by Geoff Warburton,
                                Scott Harris, Shawn Mendes, and Teddy Geiger, who also produced it. It was the lead single from his third album,
                                "Shawn Mendes" (2018). "I know people who suffer from anxiety and find it a bit difficult to understand.
                                The whole reason I wrote this song is to like, ultimately, it’s not in my blood to do that (be anxious),"
                                said Mendes about the song. "In My Blood" was nominated for Song of the Year at the 2019 Grammy Awards.
                                Here’s a snippet of the lyrics:
                                "I'm overwhelmed and insecure,
                                give me something I could take to ease my mind slowly
                                Just have a drink and you'll feel better
                                Just take her home and you'll feel better
                                Keep telling me that it gets better
                                Does it ever?"                                
                            </p>

                            <h2>7. Lady Gaga - 911</h2>
                            <p> "911" is a song by Lady Gaga and is the eighth track on her album "Chromatica" (2020). It was written by Gaga along with Justin Tranter, BloodPop, and Madeon. In an Instagram post in September 2020, Lady Gaga acknowledged that the music video for "911" was her way of sharing her experiences with mental health. "This short film is very personal to me, my experience with mental health and how reality and dreams can interconnect to form heroes within us and around us," Gaga said. She also mentioned that the song is very honest about her mental health condition. Here’s a snippet of the lyrics:
                                "Turnin' up emotional faders
                                Keep repeating self-hating phrases
                                I have heard enough of these voices
                                Almost like I have no choice
                                This is biological stasis"
                            </p>

                            <h2>8. Jason Isbell – Anxiety</h2>
                            <p>"Anxiety" is a composition that addresses the impact of mental illness, particularly how those who suffer from it must also grapple with other people’s perceptions of their experiences. “I don’t have clinically diagnosed anxiety problems or paralyzing attacks where I can’t function,” said Isbell. “But I wanted to cover and represent that aspect in the song. So, I turned to my wife, who has more experience with these things, and we co-wrote the song. I wanted to be specific and depict people’s experiences when they have moments of disconnection from reality, and things get heavy. So, I consulted her about it.”
                            </p>

                            <h2>9. Blaine Larsen – How Do You Get That Lonely</h2>
                            <p> Written as a tribute and dedication to remembering Lance Emmitt who committed suicide at age 19, the lyrics reflect on the reasons behind how and why the boy made this decision. The song conveys the idea that a person's choice to end their life impacts more than just themselves; the entire community is negatively affected by this decision. "I got chills the first time I heard it, and I knew it was a song I had to cut," said Larsen. "I was deciding between a few songs for the record, choosing different ones, and I went home and received a lot of emails, one of which said, 'Your song prevented me from committing suicide.' I got emotional reading that email. It really opened my eyes to how a song can do that."
                            </p>

                            <h2>10. Tyler Joe Miller – Fighting</h2>
                            <p>"Fighting" emphasizes the message of taking life one day at a time, a mantra for many people regardless of their personal or professional situations. Miller said the song is “about revealing the skeletons in your closet and the demons under your bed that everyone has, and this song is about fighting yourself and your insecurities to overcome them and try to be a better person.” Since releasing the song in December 2020, Miller has received many messages from people sharing their personal stories and struggles. He added, “Fighting quickly stopped being about me and my story, and became about everyone who relates to it and writes their own struggles into it... Cutting through the BS and talking about real issues and struggles is hard, but I’m glad this song can help open up the conversation to break that stigma a bit. Remember, if you’re struggling, you’re in the majority.”
                            </p>
