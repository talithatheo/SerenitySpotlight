                            <h3>Anxiety</h3>
                            <p>Emotions with feelings of tension and worried thoughts.</p>
                            <div class="read-more-cont">
                                <p>It’s a human body’s natural response to stress, and everyone experiences it. It’s a group
                                    of sensations that are generally unpleasant and mainly arise from being consciously worried
                                    about the future, or afraid of an actual situation. Anxiety includes a variety of physical
                                    sensations that are linked with thoughts that make a person feel apprehensive or fearful
                                    about what’s to come. It’s a signal that we need to adapt to life’s challenges by learning
                                    how to cope. If anxiety is persistent or out of proportion to the situation, it can be
                                    a symptom of poor mental health.</p>
