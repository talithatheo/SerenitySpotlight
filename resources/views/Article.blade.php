                            <h3>Mental Disorder</h3>
                            <p>Health condition affecting mental and daily function.</p>
                            <div class="read-more-cont">
                                <p>Mental disorder, also referred to as mental illness, are behavioral or mental patterns that cause
                                    significant distress or impairment of personal functioning. They are diagnosed by a trained health
                                    professional, such as a doctor, clinical psychologist, or psychiatrist, using internationally
                                    established diagnostic criteria. The Diagnostic and Statistical Manual of Mental Disorders,
                                    Fifth Edition (DSM-5) lists almost 300 different mental health disorders and organizes them
                                    into broader categories.</p>

                                <p>Mental disorders are viewed as irregularities or interruptions of the normal functioning of
                                    the mind or body. They may be caused by different factors, for example, genetic predispositions
                                    and family history of other diseases, traumatic life experiences, etc. But mental disorders
                                    can’t be detected by laboratory tests, and their symptoms vary widely and overlap considerably.
                                    For instance, depression and anxiety share the majority of symptoms and also tend to co-occur
                                    in what is called comorbidity.</p>
                                    
                                <p>Mental disorder disrupt a person’s life, adversely impacting their emotional, physical, and
                                    mental health and well-being. In most cases, symptoms of mental disorders can be effectively
                                    managed with a combination of medications and psychotherapy.</p>
