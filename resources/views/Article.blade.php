                            <p>Sudden attacks of panic or intense fear.</p>
                            <div class="read-more-cont">
                                <p>A sudden experience of intense fear or psychological and physical discomfort that develops
                                    for no apparent reason and that includes physical symptoms such as dizziness, trembling,
                                    sweating, difficulty breathing, or increased heart rate. Occasional panic attacks are normal.
                                    If they become persistent and severe, the person can develop a panic disorder.</p>
