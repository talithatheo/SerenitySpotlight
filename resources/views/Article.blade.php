                            <h3>Negative Impact</h3>
                            <p>Becoming a less good or even bad person</p>
                            <div class="read-more-cont">
                                <h2>1. Decreased Quality of Life</h2>
                                <p>Mental disorders can significantly lower a person's quality of life. Individuals may struggle with daily activities, maintaining relationships, and achieving personal and professional goals.</p>
                                
                                <h2>2. Stigma and Discrimination</h2>
                                <p>Many people with mental disorders face stigma and discrimination from society. This can hinder them from seeking the necessary help and support, worsening their mental condition.</p>

                                <h2>3. Social Isolation</h2>
                                <p>Mental disorders often lead to social isolation, where individuals withdraw from friends and family. This can exacerbate feelings of loneliness and make recovery more difficult.</p>

                                <h2>4. Physical Health Problems</h2>
                                <p>People with mental disorders frequently experience physical health issues related to chronic stress and poor self-care. These can include sleep disorders, cardiovascular diseases, and eating disorders.</p>

                                <h2>5. Loss of Productivity</h2>
                                <p>Mental disorders can reduce productivity and performance at work or school. This can impact financial stability and career development.</p>

                                <h2>6. Substance Abuse Risk</h2>
                                <p>Some individuals with mental disorders may turn to drug or alcohol use as a way to cope with their symptoms. This can lead to addiction and additional health problems.</p>

                                <h2>7. Suicide Risk</h2>
                                <p>Severe mental disorders can increase the risk of suicide. Feelings of hopelessness and overwhelming burden often make individuals feel that suicide is the only way out.</p>
