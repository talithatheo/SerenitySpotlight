                            <h3>Deppression</h3>
                            <p>A mental health condition that causes chronic feelings of sadness.</p>
                            <div class="read-more-cont">
                                <p>A term is used to describe a state of low mood or a mental disorder (also called major
                                    depressive disorder or clinical depression). Clinical depression is a common serious mood
                                    disorder that causes severe symptoms, such as sadness and feelings of worthlessness or
                                    excessive guilt, lack of interest or pleasure in daily activities.</p>

                                <p>The symptoms affect how you feel, think, and handle daily activities, such as eating,
                                    sleeping, studying, or working. A person can be diagnosed with depression if the symptoms
                                    are present for at least two weeks.</p>
